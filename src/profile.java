
public class profile {
	private String namaField;
	private String alamatField;
	private String usernameField;
	private String passwordField;
	private Boolean Role;

	public String getNamaField() {
		return namaField;
	}

	public void setNamaField(String namaField) {
		this.namaField = namaField;
	}

	public String getAlamatField() {
		return alamatField;
	}

	public void setAlamatField(String alamatField) {
		this.alamatField = alamatField;
	}

	public String getUsernameField() {
		return usernameField;
	}

	public void setUsernameField(String usernameField) {
		this.usernameField = usernameField;
	}

	public String getPasswordField() {
		return passwordField;
	}

	public void setPasswordField(String passwordField) {
		this.passwordField = passwordField;
	}

	public Boolean getRole() {
		return Role;
	}

	public void setRole(Boolean role) {
		Role = role;
	}
	
	
}
